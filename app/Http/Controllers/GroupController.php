     */
    public function store(Request $request)
    {
        //
        /*  */
        //
        // dont forget to validate
        //
        $group = new Group;
        $group->name = $request->name;
        $group->description = $request->description;
        $group->save();
        if ($request->is('api/*')) {
            return response()->json($group);
        }
        return redirect()->route('group.index');
    }

